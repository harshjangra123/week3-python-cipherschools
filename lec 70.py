# {1(Start argument) , 4(Stop argument) , 8(Step argument)}
# print for odd number
for i in range (1,11,2):
    print(i)
# print for even number
for i in range (0,11,2):
    print(i)
# print 10 , 9 , 8 , upto 1
for i in range(10,0,-1):
    print(i)
# print 10 , 9 , 8 , upto 0
for i in range(10,-1,-1):
    print(i)